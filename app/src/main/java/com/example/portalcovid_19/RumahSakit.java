package com.example.portalcovid_19;

public class RumahSakit {
    private String namaRS;
    private String alamatRS;
    private String koordinatRS;
    private String nomorRS;
    private int fotoRS;

    public String getNamaRS() {
        return namaRS;
    }

    public void setNamaRS(String namaRS) {
        this.namaRS = namaRS;
    }

    public String getAlamatRS() {
        return alamatRS;
    }

    public void setAlamatRS(String alamatRS) {
        this.alamatRS = alamatRS;
    }

    public String getKoordinatRS() {
        return koordinatRS;
    }

    public void setKoordinatRS(String koordinatRS) {
        this.koordinatRS = koordinatRS;
    }

    public String getNomorRS() {
        return nomorRS;
    }

    public void setNomorRS(String nomorRS) {
        this.nomorRS = nomorRS;
    }

    public int getFotoRS() {
        return fotoRS;
    }

    public void setFotoRS(int fotoRS) {
        this.fotoRS = fotoRS;
    }
}
